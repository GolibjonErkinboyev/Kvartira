class Flat:
    def init(self, code, area):
        self.code = code
        self.area = area


    def getCode(self):
        return self.code

    def getDimension(self):
        return self.area

    def setTariffs(self, tariffs):
        self.tariffs = tariffs

    def getTariffs(self):
        return self.tariffs
    

